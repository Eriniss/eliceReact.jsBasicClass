// * Create React App 소개


// CRA란?
// React 프로젝트를 손쉽게 생성할 수 있도록 도와주는 '보일러플레이트'
// 수많은 React용 보일러플레이트가 있지만 Facebook에서 직접 만들어서 관리하는 Create React App이 가장 많이 쓰임
// 프로젝트 생성에 필요한 다양한 기능을 Command로 제공함


// CRA의 장점
// 1. 개발자가 온전히 React App 개발에 집중할 수 있도록 함
// 2. 모든 브라우저에서 해석될 수 있도록 transcompile 지원


// npm, 많이 사용하는 명령어 모음

// npm install
// package.json에 정의된 dependency(의존성 패티지)들을 설치합니다.

// npm install <패키지명>
// npm 서버로부터 원하는 패키지를 내려 받습니다.

// npm start
// 프로젝트를 실행합니다.(Node.js 서버 이용)

// npm build
// 프로젝트를 빌드합니다.

// (Ctrl + C)
// 명령을 중지합니다.


// 디렉토리 구조

// ./node_modules/
// npm을 이용해 설치한 패키지들 모음

// ./public/
// 정적인 파일들을 모아 놓는 곳

// ./src/
// 리액트 개발을 위한 파일들을 모아 놓는 곳

// ./.gitignore
// git을 이용할 경우 불필요한 파일을 무시할 수 있도록 하는 설정 파일

// ./package.json
// 프로젝트에 관한 정보와 사용하는 패키지들을 명세하는 파일

// ./README.md
// 내 프로젝트에 관한 설명을 작성하는 파일